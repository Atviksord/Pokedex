func commandMapb(c *Config, newcache *pokecache.Cache, d string) error {
	if c.Previous != "" {
		apiCallHandler(true, c, newcache)
	} else {
		fmt.Println("Nothing to go back to")
	}
	return nil
}
func commandExplore(c *Config, newcache *pokecache.Cache, areaName string) error {
	if areaName != "" {
		for _, areas := range c.Results {
			if areas.Name == areaName {
				fmt.Printf("Exploring: %s ... \n", areas.Name)
				fmt.Println("Found pokemon:")
				cachedArea, ok := newcache.Get(areas.URL)
				if !ok { // if not in cache get it.
					response, err := http.Get(areas.URL)
					if err != nil {
						log.Fatal(err)
					}
					body, err := io.ReadAll(response.Body)
					if err != nil {
						log.Fatal(err)
					}

					var areaStruct AreaResponse
					err = json.Unmarshal(body, &areaStruct)
					if err != nil {
						fmt.Println(err)
					}
					newcache.Add(areas.URL, body)
					for _, encounter := range areaStruct.PokemonEncounters {
						pokemonName := encounter.Pokemon.Name
						fmt.Printf("- %s \n", pokemonName)

					}

				} else {
					var areaStruct AreaResponse
					err := json.Unmarshal(cachedArea, &areaStruct)
					if err != nil {
						fmt.Println(err)
					}
					for _, encounter := range areaStruct.PokemonEncounters {
						pokemonName := encounter.Pokemon.Name
						fmt.Printf("- %s \n", pokemonName)
					}

				}

			}

		}
	}

	return nil
}
func getCommands() map[string]cliCommand {

	return map[string]cliCommand{
		"help": {
			name:        "help",
			description: "Displays a help message",
			callback:    commandHelp,
		},
		"exit": {
			name:        "exit",
			description: "Exit the Pokedex",
			callback:    commandExit,
		},
		"map": {
			name:        "map",
			description: "displays the names of 20 location areas in the Pokemon world",
			callback:    commandMap,
		},
		"mapb": {
			name:        "mapb",
			description: "displays the previous 20 locations in the Pokemon world",
			callback:    commandMapb,
		},
		"explore": {
			name:        "explore",
			description: "explores a selected area",
			callback:    commandExplore,
		},
		"catch": {
			name:        "catch",
			description: "catches selected pokemon",
			callback:    commandCatch,
		},
	}
}

func main() {
	commandmap := getCommands()
	var c Config
	newcache := pokecache.NewCache(1 * time.Minute)

	for {
		fmt.Print("Pokedex > ")
		scanner := bufio.NewScanner(os.Stdin)
		scanner.Scan()
		line := scanner.Text()
		words := strings.Fields(line)

		if cmd, ok := commandmap[words[0]]; ok {
			if len(words) > 1 {
				err := cmd.callback(&c, newcache, words[1])
				if err != nil {
					fmt.Println("There was an error executing the commando:", err)
				}
			} else {
				err := cmd.callback(&c, newcache, "")
				if err != nil {
					fmt.Println("There was an error executing the commando:", err)
				}

			}
		}
	}

}
